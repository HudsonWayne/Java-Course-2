package com.example;

public class ConditionalStatements {
    public static void main(String[] args) {
        int age = 18;
        if(age>=18){
            System.out.println("You are an adult");
        }

        int marks = 45;
        if(marks>=50){
            System.out.println("You passed");
        }else{
            System.out.println("You failed");
        }
    }
}
